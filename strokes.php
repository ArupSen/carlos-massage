	<h2>Types of Strokes</h2>
	<h3>Petrissage</h3>
	<p>Petrissage or kneading is used on deeper tissues for mobilising fluids, stretching muscle fibres, and inducing relaxation. Some of these techniques are specifically aimed at only deeper tissue and are therefore both ineffective and difficult to perform on narrow parts of the limbs. You must therefore pay particular attention, as with all massage techniques, as to what your purpose is when kneading petrissage is a powerful technique that is particularly effective in mobilising fluids in very deep muscles and applying a good stretch to the fibres involved.</p>
	<h3>Frictions</h3>
<p>Frictions may be used for exploratory purposes, or for deeper and sometimes more painful movements aimed at breaking down lesions, separating muscle fibres, and even breaking down recent scar tissue. When performing frictions for exploratory purposes we tend to use the sensory pad of the thumb to "grasp" the skin and to move this over the underlying surface to feel for any abnormalities (trigger points) to the tissue concerned. This allows us to palpate, particularly around joints and feel for smaller abnormalities in the tissues. When performing frictions for the purpose of separating muscle fibres, breaking down lesions and scar tissues, firstly the client must be warned that these procedures may indeed be painful or at the very least uncomfortable. They only need to be carried out for a very short while. Using vigorous frictions for any longer than this may have a particularly detrimental effect by irritating and even causing inflammation. </p>
<h3>Stroking (Effleurage)</h3>
<p>The simplest and most natural of all massage strokes, Effleurage is perfect for openers. You simply glide your hands across the body, using flat hands on the back and cupped hands on the legs and arms. The rhythmic stroking movement not only relaxes the person receiving the massage and lets him or her get used to the massager's hands, but readies the body for deeper work. 
	</p>
	<h3>Knuckling</h3>
	<p>In Circular Knuckling, often used on the upper chest and shoulders, you press your knuckles into the muscles and use a rotating motion to ease tightness and tension. Straight Knuckling, in which your knuckles are kept in a line (like in a loose fist) and worked smoothly along the muscles, works well on the buttocks and thighs.
	</p>
	<h3>Tapping (Tapotement)</h3>
	<p>A mainstay of Swedish massage, Tapotement refers to a number of percussive movements. In Pummeling, you pound the fleshy areas of the body (buttocks and thighs) with the sides of your loose fists, dispersing tension and stimulating blood flow. Similar to this is Hacking, where you use the edges of your flattened hands in a light karate-chop motion. Keep your hands loose and your wrists relaxed and maintain a slow rhythmic pace. These strokes can be effective on muscles that a strained or cramped. 
	</p>
	<h3>Vibration</h3>
	<p>This involves shaking the muscles rapidly, using flattened hands or fingers. Use this stroke sparingly to promote circulation.</p>
